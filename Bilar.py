class Bil:
    

    def __init__(self, Fabrikat, Color, Lager):
        self.Fabrikat = Fabrikat
        self.Color = Color
        self.Lager = Lager

    def set_Lager(self, Lager):
        self.Lager = Lager

    def get_Lager(self):
        return self.Lager
    
    def get_Fabrikat(self):
        return self.Fabrikat
    
    def get_Color(self):
        return self.Color
    
